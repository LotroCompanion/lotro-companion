package delta.games.lotro.gui.lore.items.chooser;

/**
 * Identifier for the components of the item filter UI.
 * @author DAM
 */
public enum ItemChooserFilterComponent
{
  /**
   * Essence tier filter.
   */
  TIER,
  /**
   * Name filter.
   */
  NAME,
  /**
   * Quality filter.
   */
  QUALITY,
  /**
   * Category filter.
   */
  CATEGORY,
  /**
   * Legendary filter.
   */
  LEGENDARY,
  /**
   * Stats filter.
   */
  STAT,
  /**
   * Armour type filter.
   */
  ARMOUR_TYPE,
  /**
   * Shield type filter.
   */
  SHIELD_TYPE,
  /**
   * Weapon type filter.
   */
  WEAPON_TYPE,
  /**
   * Damage type filter.
   */
  DAMAGE_TYPE,
  /**
   * Weapon slayer genus filter.
   */
  SLAYER_GENUS,
  /**
   * Filter on the class of the current character.
   */
  CURRENT_CHAR_CLASS,
  /**
   * Filter on the proficiencies of the current character.
   */
  CURRENT_CHAR_PROFICIENCIES,
  /**
   * Filter on the level of the current character.
   */
  CURRENT_CHAR_LEVEL,
  /**
   * Item level filter.
   */
  ITEM_LEVEL,
  /**
   * Scalable filter.
   */
  SCALABLE,
  /**
   * Generic character class filter.
   */
  GENERIC_CHARACTER_CLASS,
  /**
   * Generic character race filter.
   */
  GENERIC_CHARACTER_RACE,
  /**
   * Equipment location.
   */
  LOCATION
}
