  /**
   * Get the icon path for a given profession and tier.
   * @param professionStr Profession.
   * @param tier Tier, starting at 1.
   * @return An icon or <code>null</code> if not found.
   */
  public static String getIcon(String professionStr, int tier)
  {
    String ret=null;
    Profession profession=Profession.getByLabel(professionStr);
    if (profession!=null)
    {
      int index=Profession.getAll().indexOf(profession);
      if (index!=-1)
      {
        String[] recipeIconsForProfession=recipeIcons[index];
        if (tier<=recipeIconsForProfession.length)
        {
          ret=recipeIconsForProfession[tier-1];
        }
      }
    }
    return ret;
  }
